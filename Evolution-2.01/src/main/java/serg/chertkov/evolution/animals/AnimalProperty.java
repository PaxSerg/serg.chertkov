public class AnimalProperty {
    public int x;
    public int y;
    public int nrg;
    public int age;
    public int position;

    public AnimalProperty(int x, int y, int nrg, int age, int position){
        this.x = x;
        this.y = y;
        this.nrg = nrg;
        this.age = age;
        this.position = position;
    }
}
